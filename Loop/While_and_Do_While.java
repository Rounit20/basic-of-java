package Loop;
public class While_and_Do_While
{
    public static void main(String[] args)
    { int i=0;
        while(i<5)
        {
            System.out.println("WHILE LOOP");
            i++;
        }


    // do while loop:-

        int j=0;
        do
        {
            System.out.println("DO WHILE LOOP");
            j++;

        }while(j<5);
    }
}
