package Operator;
public class TYPE_CASTING
{
    public static void main(String[] args)
    {
       int a=45;
       double b;
       b=a;
        System.out.println(b);

        double num1=45.5;
        int num2;
        num2=(int)num1;
        System.out.println(num2);

        int n1=16;
        int n2=5;
        float result=n1/n2;
        System.out.println(result);

    }
}