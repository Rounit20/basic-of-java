package Operator;
public class RELATIONAL_OPERATOR
{
    public static void main(String[] args)
    {
        // relational operator  ==,<,>,<=,>=

        int a=10;
        int b=10;
        int c=20;
        boolean result=a==b;
        System.out.println(result);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a<=c);
        System.out.println(a!=b);

        //note :- we can use both ways
                   // != implies not equal to
    }
}
